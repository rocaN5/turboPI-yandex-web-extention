(function() {
    const urlPattern = /^https:\/\/logistics\.market\.yandex\.ru\/sorting-center\/\d+\/sortables/;
    if (!urlPattern.test(location.href)) return;

    function getOrderWord(count) {
        if (count % 10 === 1 && count % 100 !== 11) return 'заказ';
        if (count % 10 >= 2 && count % 10 <= 4 && (count % 100 < 10 || count % 100 >= 20)) return 'заказа';
        return 'заказов';
    }

    function getPageFromUrl() {
        const url = new URL(window.location.href);
        const page = url.searchParams.get('page');
        return page || '1';
    }

    function updatePageDisplay() {
        const pageElement = document.querySelector('.tpi-sortables-data-current-page-data');
        if (pageElement) {
            pageElement.textContent = getPageFromUrl();
        }
    }

    function getTableData() {
        const tableBody = document.querySelector('tbody[data-tid-prop="60ba3905"][data-tid="1544a960 60ba3905"]');
        if (!tableBody) return { items: [], count: 0 };

        const rows = tableBody.querySelectorAll('tr');
        const items = [];

        rows.forEach(row => {
            const sortableElement = row.querySelector('a[data-e2e="sortable-barcode-link"] div');
            const sortableText = sortableElement ? sortableElement.textContent.trim() : '';

            const orderElement = row.querySelector('a[data-e2e="sortable-table-order-external-id"] span');
            const orderText = orderElement ? orderElement.textContent.trim() : '';

            const anomalyElement = row.querySelector('a span[data-e2e-i18n-key="common.sorting-center:sortable-type.ANOMALY"]');
            let anomalyId = '';
            if (anomalyElement) {
                const anomalyLink = anomalyElement.closest('a');
                if (anomalyLink) {
                    const href = anomalyLink.getAttribute('href');
                    const match = href.match(/ANOMALIISC-(\d+)/);
                    if (match) {
                        anomalyId = match[0];
                    }
                }
            }

            const lotElement = row.querySelector('a[data-e2e^="sortable-table-inner-sortable-count-"]');
            let lotInnerData = '';
            if (lotElement) {
                lotInnerData = lotElement.textContent.trim();
            }

            const startsWithFA254 = sortableText.startsWith('FA254');
            const startsWithFLot = sortableText.startsWith('F1254') || sortableText.startsWith('F300');

            items.push({
                sortable: sortableText,
                order: orderText,
                anomalyId: anomalyId,
                lotInnerData: lotInnerData,
                startsWithFA254: startsWithFA254,
                startsWithFLot: startsWithFLot
            });
        });

        return { items, count: items.length };
    }

    function formatForCopy(items, isMono) {
        return items.map(item => {
            if (item.order) {
                if (isMono) {
                    return `\`${item.order}\` (\`${item.sortable}\`)`;
                } else {
                    return `${item.order} (${item.sortable})`;
                }
            }
            else if (item.startsWithFA254 && item.anomalyId) {
                if (isMono) {
                    return `\`${item.sortable}\` (\`${item.anomalyId}\`)`;
                } else {
                    return `${item.sortable} (${item.anomalyId})`;
                }
            }
            else if (item.startsWithFLot) {
                if (item.lotInnerData) {
                    const orderCount = parseInt(item.lotInnerData) || 0;
                    const orderWord = getOrderWord(orderCount);
                    
                    if (isMono) {
                        return `\`${item.sortable}\` (**${item.lotInnerData} ${orderWord}**)`;
                    } else {
                        return `${item.sortable} (${item.lotInnerData} ${orderWord})`;
                    }
                } else {
                    return isMono ? `\`${item.sortable}\`` : item.sortable;
                }
            }
            else {
                return isMono ? `\`${item.sortable}\`` : item.sortable;
            }
        }).join('\n');
    }

    function copyToClipboard(text, type, count) {
        navigator.clipboard.writeText(text).then(() => {
            const countSpan = `<span>${count}</span>`;
            
            if (type === 'default') {
                tpiNotification.show(
                    "Номера скопированы",
                    "success",
                    `Скопировано в буфер обмена ${countSpan} ${getOrderWord(count)}.`
                );
            } else {
                tpiNotification.show(
                    "Номера скопированы",
                    "success",
                    `Скопировано в буфер обмена ${countSpan} ${getOrderWord(count)} как <span>моно</span>.`
                );
            }
        }).catch(() => {
            tpiNotification.show("Ошибка", "error", "Не удалось скопировать данные");
        });
    }

    function handleButtonClick(action) {
        const { items, count } = getTableData();
        
        if (count === 0) {
            tpiNotification.show("Внимание", "warning", "Нет данных для копирования");
            return;
        }

        const isMono = action === 'copy-mono';
        const formattedText = formatForCopy(items, isMono);
        copyToClipboard(formattedText, isMono ? 'mono' : 'default', count);
    }

    function updatePaginationButtonsState() {
        const leftButton = document.querySelector('[tpi-sortables-action="switch-left"]');
        const rightButton = document.querySelector('[tpi-sortables-action="switch-right"]');
        const paginationWrapper = document.querySelector('.tpi-sortables-page-switch-wrapper');
        
        if (!leftButton || !rightButton || !paginationWrapper) return;
        
        const originalLeft = document.querySelector('button[icon="chevronLeft"]');
        const originalRight = document.querySelector('button[icon="chevronRight"]');
        
        if (!originalLeft || !originalRight) {
            paginationWrapper.style.display = 'none';
            return;
        }
        
        paginationWrapper.style.display = '';
        
        leftButton.disabled = originalLeft.disabled || originalLeft.getAttribute('aria-disabled') === 'true';
        rightButton.disabled = originalRight.disabled || originalRight.getAttribute('aria-disabled') === 'true';
    }

    function addButtons() {
        const targetSpan = document.querySelector('span[data-e2e-i18n-key="pages.sorting-center-sortable-list:stage-report-page-link"]');
        
        if (!targetSpan) return;
        
        const parentDiv = targetSpan.closest('div');
        
        if (!parentDiv) return;
        
        parentDiv.classList.add('tpi-sortables-contols-wrapper');
        
        const link = parentDiv.querySelector('a');
        
        if (!link) return;
        
        if (link.nextElementSibling && link.nextElementSibling.classList.contains('tpi-sortables-contols-copy-data')) {
            return;
        }
        
        const buttonsWrapper = document.createElement('div');
        buttonsWrapper.className = 'tpi-sortables-buttons-wrapper';
        
        const tpi_st_copy_default = document.createElement('button');
        tpi_st_copy_default.className = 'tpi-sortables-contols-copy-data';
        tpi_st_copy_default.setAttribute('tpi-sortables-action', 'copy-default');
        tpi_st_copy_default.setAttribute('tpi-tooltip-data', 'Скопировать все номера заказов из таблицы');
        tpi_st_copy_default.innerHTML = `<icon class="tpi-sortables-controls-copy-icon">${tpi_st_i_copy_default}</icon>`;
        
        const tpi_st_copy_mono = document.createElement('button');
        tpi_st_copy_mono.className = 'tpi-sortables-contols-copy-data';
        tpi_st_copy_mono.setAttribute('tpi-sortables-action', 'copy-mono');
        tpi_st_copy_mono.setAttribute('tpi-tooltip-data', 'Скопировать все номера заказов из таблицы для телеграмм');
        tpi_st_copy_mono.innerHTML = `<icon class="tpi-sortables-controls-copy-icon">${tpi_st_i_copy_mono}</icon>`;
        
        tpi_st_copy_default.addEventListener('click', () => handleButtonClick('copy-default'));
        tpi_st_copy_mono.addEventListener('click', () => handleButtonClick('copy-mono'));
        
        const tip_st_page_wrapper = document.createElement('div');
        tip_st_page_wrapper.className = 'tpi-sortables-page-switch-wrapper';

        const tip_st_page_button_left = document.createElement('button');
        tip_st_page_button_left.className = 'tpi-sortables-contols-switch-page';
        tip_st_page_button_left.setAttribute('tpi-sortables-action', 'switch-left');
        tip_st_page_button_left.innerHTML = `<icon class="tpi-sortables-controls-copy-icon">${tpi_st_i_chevron_left}</icon>`;

        const tpi_st_current_page_wrapper = document.createElement('div');
        tpi_st_current_page_wrapper.className = 'tpi-sortables-data-current-page';
        
        const currentPageData = document.createElement('p');
        currentPageData.className = 'tpi-sortables-data-current-page-data';
        currentPageData.textContent = getPageFromUrl();
        tpi_st_current_page_wrapper.appendChild(currentPageData);

        const tip_st_page_button_right = document.createElement('button');
        tip_st_page_button_right.className = 'tpi-sortables-contols-switch-page';
        tip_st_page_button_right.setAttribute('tpi-sortables-action', 'switch-right');
        tip_st_page_button_right.innerHTML = `<icon class="tpi-sortables-controls-copy-icon">${tpi_st_i_chevron_right}</icon>`;

        tip_st_page_wrapper.appendChild(tip_st_page_button_left);
        tip_st_page_wrapper.appendChild(tpi_st_current_page_wrapper);
        tip_st_page_wrapper.appendChild(tip_st_page_button_right);

        buttonsWrapper.appendChild(tpi_st_copy_default);
        buttonsWrapper.appendChild(tpi_st_copy_mono);
        buttonsWrapper.appendChild(tip_st_page_wrapper);
        
        link.insertAdjacentElement('afterend', buttonsWrapper);

        initTooltips()

        // Функция для обновления номера страницы с предиктивным изменением
        function updatePageNumber(delta) {
            const pageElement = document.querySelector('.tpi-sortables-data-current-page-data');
            if (!pageElement) return;
            
            const currentPage = parseInt(pageElement.textContent) || 1;
            const newPage = currentPage + delta;
            if (newPage >= 1) {
                pageElement.textContent = newPage;
            }
        }

        tip_st_page_button_left.addEventListener('click', function() {
            const originalLeft = document.querySelector('button[icon="chevronLeft"]');
            if (originalLeft && !originalLeft.disabled && originalLeft.getAttribute('aria-disabled') !== 'true') {
                updatePageNumber(-1);
                originalLeft.click();
            }
        });

        tip_st_page_button_right.addEventListener('click', function() {
            const originalRight = document.querySelector('button[icon="chevronRight"]');
            if (originalRight && !originalRight.disabled && originalRight.getAttribute('aria-disabled') !== 'true') {
                updatePageNumber(1);
                originalRight.click();
            }
        });

        // Наблюдатель за изменениями в body
        const observer = new MutationObserver(function(mutations) {
            // Проверяем, изменились ли кнопки пагинации
            let shouldUpdate = false;
            
            for (const mutation of mutations) {
                if (mutation.type === 'childList') {
                    // Проверяем добавление/удаление кнопок
                    const addedButtons = Array.from(mutation.addedNodes).some(node => 
                        node.nodeType === 1 && (node.matches('button[icon="chevronLeft"], button[icon="chevronRight"]') ||
                        node.querySelector('button[icon="chevronLeft"], button[icon="chevronRight"]'))
                    );
                    
                    const removedButtons = Array.from(mutation.removedNodes).some(node => 
                        node.nodeType === 1 && (node.matches('button[icon="chevronLeft"], button[icon="chevronRight"]') ||
                        node.querySelector('button[icon="chevronLeft"], button[icon="chevronRight"]'))
                    );
                    
                    if (addedButtons || removedButtons) {
                        shouldUpdate = true;
                        break;
                    }
                }
                
                if (mutation.type === 'attributes') {
                    if (mutation.target.matches && mutation.target.matches('button[icon="chevronLeft"], button[icon="chevronRight"]')) {
                        shouldUpdate = true;
                        break;
                    }
                }
            }
            
            if (shouldUpdate) {
                updatePaginationButtonsState();
            }
        });
        
        observer.observe(document.body, {
            childList: true,
            subtree: true,
            attributes: true,
            attributeFilter: ['disabled', 'aria-disabled', 'class']
        });

        // Обновляем состояние при загрузке
        updatePaginationButtonsState();
    }
    
    // Запускаем с задержкой для полной загрузки DOM
    if (document.readyState === 'loading') {
        document.addEventListener('DOMContentLoaded', () => setTimeout(addButtons, 500));
    } else {
        setTimeout(addButtons, 500);
    }
    
    // Навигация
    let lastUrl = location.href;
    
    function checkUrlChange() {
        const currentUrl = location.href;
        if (currentUrl !== lastUrl) {
            lastUrl = currentUrl;
            updatePageDisplay();
            updatePaginationButtonsState();
        }
    }
    
    // Используем MutationObserver для отслеживания изменений URL через history API
    const originalPushState = history.pushState;
    const originalReplaceState = history.replaceState;
    
    history.pushState = function() {
        originalPushState.apply(this, arguments);
        checkUrlChange();
    };
    
    history.replaceState = function() {
        originalReplaceState.apply(this, arguments);
        checkUrlChange();
    };
    
    window.addEventListener('popstate', checkUrlChange);
    
    // Также наблюдаем за изменениями в title (часто меняется при смене страницы в SPA)
    const titleObserver = new MutationObserver(checkUrlChange);
    const titleElement = document.querySelector('title');
    if (titleElement) {
        titleObserver.observe(titleElement, { childList: true, subtree: true });
    }
})();